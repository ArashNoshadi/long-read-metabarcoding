# Function to extract the main name (first word) from the Organism string
# ------------------------------------------------------------------------------
def extract_main_name(organism):
    organism = str(organism)
    m = main_name_pattern.match(organism.lower())
    if m:
        return m.group(1)
    return organism.lower()

# -------------------------------------------------------------------------------
# تابع تعیین خانواده بر اساس وجود جنس (genus) در متن Organism
# -------------------------------------------------------------------------------
def find_family(organism):
    organism = str(organism)
    words = set(organism.lower().split())
    # اگر کل متن organism به عنوان نام خانواده موجود است
    if organism.lower() in lower_nematode_data:
        return organism.lower()
    # بررسی وجود هر یک از جنس‌ها در کلمات
    for family, genera in lower_nematode_data.items():
        if words & genera:  # تقاطع مجموعه‌ها
            return family
    return 'unknown'

# -------------------------------------------------------------------------------
# تابع دریافت جزئیات خانواده (ستون‌های cp و f-h)
# -------------------------------------------------------------------------------
def get_family_details(family):
    details = detailed_family_data_lower.get(family.lower(), {'cp': 'Unknown', 'f-h': 'Unknown'})
    return details['cp'], details['f-h']

# -------------------------------------------------------------------------------
# تابع استخراج گونه (Species) از Organism
# -------------------------------------------------------------------------------
def extract_species(organism):
    organism = str(organism)
    parts = organism.split()
    if not parts:
        return ""
    if parts[0].lower() == 'unknown':
        return "unknown"
    if len(parts) < 2:
        return ''
    species = ' '.join(parts[1:]).strip()
    if species.startswith('sp.'):
        return ''
    return species

