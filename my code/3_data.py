# تابع تعیین وضعیت تعداد کلمات گونه: 1 اگر بیش از یک کلمه باشد، در غیر این صورت 0
# -------------------------------------------------------------------------------
def species_word_flag(species):
    # با استفاده از شمارش فاصله می‌توانیم تعیین کنیم آیا بیش از یک کلمه هست یا خیر
    return 1 if species.strip().count(' ') >= 1 else 0

