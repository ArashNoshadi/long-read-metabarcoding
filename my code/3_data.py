# تابع پردازش فایل اکسل با استفاده از توابع بالا
# -------------------------------------------------------------------------------
def process_file(input_file_path, output_file_path):
    df = pd.read_excel(input_file_path)
    
    # اطمینان از اینکه ستون Organism به رشته تبدیل شود
    df['Organism'] = df['Organism'].astype(str)
    
    df['Main_Organism'] = df['Organism'].apply(extract_main_name)
    df['Family'] = df['Main_Organism'].apply(find_family)
    df['cp'], df['f-h'] = zip(*df['Family'].apply(get_family_details))
    df['Species'] = df['Organism'].apply(extract_species)
    df['Species_Word_Flag'] = df['Species'].apply(species_word_flag)
    
    df.to_excel(output_file_path, index=False)
    print(f"پردازش فایل {input_file_path} تکمیل شد و خروجی در {output_file_path} ذخیره گردید.")

